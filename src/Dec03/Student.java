package Dec03;

public class Student {

    String name;
    String email;
    String course;

    public Student() {
    }

    public void Student (String name, String email) {
        this.name = name;
        this.email = email;
    }
    public void Student(String name, String email, String course) {
        this.name = name;
        this.email = email;
        this.course = course;
    }

}
